export async function generateExportPack(
  messages: Message[],
  conversationTitle: string,
  caseRef: string,
  onProgress: (status: string) => void
): Promise<void> {
  // Step 1: Analyze conversation via AI
  onProgress('Analyzing conversation...');

  const headers = await getAuthHeaders();
  const response = await fetch('/api/chat', {
    method: 'POST',
    headers,
    body: JSON.stringify({
      documentGeneration: true,
      conversationHistory: messages.map((m) => ({
        role: m.role,
        content: m.content,
      })),
    }),
  });

  if (!response.ok) {
    throw new Error(`Failed to analyze conversation: ${response.status}`);
  }

  const result = await response.json();

  if (result.type !== 'document_pack') {
    throw new Error('Unexpected response type from document generation endpoint');
  }

  let packData: DocumentPackData;
  try {
    packData = parseJsonSafe(result.content);
  } catch {
    throw new Error(
      'Failed to parse document pack data from AI response. The response may be malformed.'
    );
  }

  // Step 2: Fetch report summary
  onProgress('Generating report summary...');
  const summaryResponse = await fetch('/api/report-summary', {
    method: 'POST',
    headers,
    body: JSON.stringify({
      conversationHistory: messages.map(m => ({ role: m.role, content: m.content })),
    }),
  });

  let reportSummary = {
    situation_type: 'HR Advisory',
    summary: packData.summary,
    risk_level: 'Medium',
    risk_rationale: '',
    key_legal_framework: [] as string[],
    immediate_actions: packData.actionChecklist,
    flags: [] as string[],
  };

  if (summaryResponse.ok) {
    try {
      const summaryResult = await summaryResponse.json();
      let cleaned = summaryResult.content.trim();
      const fenceMatch = cleaned.match(/```(?:json)?\s*([\s\S]*?)```/);
      if (fenceMatch) cleaned = fenceMatch[1].trim();
      reportSummary = { ...reportSummary, ...JSON.parse(cleaned) };
    } catch {}
  }

  // Step 3: Generate DOCX files
  onProgress('Generating documents...');

  const now = new Date();
  const dateStr = formatDate(now);
  const generatedDocs: GeneratedDoc[] = [];

  for (const docSpec of packData.documents) {
    const template = getTemplateById(docSpec.templateId);
    const { generateDocxBlob } = await import('./docxGenerator');

    const blob = await generateDocxBlob({
      content: template?.sections
        ? template.sections.map(s => (s.heading ? s.heading.toUpperCase() + '\n\n' : '') + s.paragraphs.join('\n')).join('\n\n')
        : docSpec.title,
      title: docSpec.title,
      tier: 2,
      category: template?.category,
      reviewNotice: '',
      caseRef: caseRef || undefined,
      filledFields: docSpec.filledFields,
    });

    const filename = sanitizeFilename(docSpec.title) + '.docx';

    generatedDocs.push({
      filename,
      title: docSpec.title,
      category: template?.category || 'General',
      blob,
    });
  }

  // Step 4: Generate binder PDF
  onProgress('Building binder PDF...');

  const pdfBlob = generateBinderPdf(
    messages,
    conversationTitle,
    caseRef,
    reportSummary,
    generatedDocs
  );

  // Step 5: Create ZIP and trigger download
  onProgress('Creating ZIP archive...');

  const zip = new JSZip();
  zip.file('binder.pdf', pdfBlob);

  const docsFolder = zip.folder('documents');
  if (docsFolder) {
    for (const genDoc of generatedDocs) {
      docsFolder.file(genDoc.filename, genDoc.blob);
    }
  }

  const zipBlob = await zip.generateAsync({ type: 'blob' });

  const prefix = caseRef ? sanitizeFilename(caseRef) : 'SITHR-export';
  const dateFileStr = formatDateFilename(now);
  saveAs(zipBlob, `${prefix}-${dateFileStr}.zip`);
}
