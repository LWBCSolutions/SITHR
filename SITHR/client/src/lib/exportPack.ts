function stripEmoji(text: string): string {
  return text
    .replace(/🟢/g, '[LOW]')
    .replace(/🟠/g, '[MEDIUM]')
    .replace(/🔴🔴/g, '[CRITICAL]')
    .replace(/🔴/g, '[HIGH]')
    .replace(/⚠️/g, '[WARNING]')
    .replace(/✅/g, '[YES]')
    .replace(/❌/g, '[NO]')
    .replace(/[\u{1F300}-\u{1F9FF}]/gu, '')
    .replace(/[\u{2600}-\u{27BF}]/gu, '');
}

interface MarkdownBlock {
  type: 'h1' | 'h2' | 'h3' | 'paragraph' | 'bold' | 'bullet' | 'numbered' | 'blockquote' | 'divider' | 'spacer';
  text?: string;
  num?: string;
}

function parseMarkdown(rawText: string): MarkdownBlock[] {
  const text = stripEmoji(rawText);
  const lines = text.split('\n');
  const blocks: MarkdownBlock[] = [];

  for (const line of lines) {
    if (line.startsWith('### ')) {
      blocks.push({ type: 'h3', text: line.replace(/^### /, '').replace(/\*\*/g, '') });
    } else if (line.startsWith('## ')) {
      blocks.push({ type: 'h2', text: line.replace(/^## /, '').replace(/\*\*/g, '') });
    } else if (line.startsWith('# ')) {
      blocks.push({ type: 'h1', text: line.replace(/^# /, '').replace(/\*\*/g, '') });
    } else if (line.startsWith('**') && line.endsWith('**')) {
      blocks.push({ type: 'bold', text: line.replace(/\*\*/g, '') });
    } else if (line.startsWith('- ') || line.startsWith('* ')) {
      blocks.push({ type: 'bullet', text: line.replace(/^[-*] /, '').replace(/\*\*(.*?)\*\*/g, '$1') });
    } else if (line.match(/^\d+\.\s/)) {
      const match = line.match(/^(\d+)\.\s(.*)$/);
      if (match) {
        blocks.push({ type: 'numbered', text: match[2].replace(/\*\*(.*?)\*\*/g, '$1'), num: match[1] });
      }
    } else if (line.startsWith('> ')) {
      blocks.push({ type: 'blockquote', text: line.replace(/^>\s*/, '').replace(/\*\*(.*?)\*\*/g, '$1') });
    } else if (line.match(/^---/) || line.match(/^___/)) {
      blocks.push({ type: 'divider' });
    } else if (line.trim() === '') {
      blocks.push({ type: 'spacer' });
    } else {
      blocks.push({ type: 'paragraph', text: line.replace(/\*\*(.*?)\*\*/g, '$1').replace(/\*(.*?)\*/g, '$1') });
    }
  }
  return blocks;
}

const PDF_MARGINS = { top: 38, bottom: 30, left: 25, right: 25 };

function renderPageHeader(doc: jsPDF): void {
  const pageWidth = doc.internal.pageSize.getWidth();
  // Blue top bar
  doc.setFillColor(45, 125, 210);
  doc.rect(0, 0, pageWidth, 6, 'F');
  // SIT-HR label
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(9);
  doc.setTextColor(45, 125, 210);
  doc.text('SIT-HR', PDF_MARGINS.left, 18);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(150, 150, 150);
  doc.text('Advisory Pack', PDF_MARGINS.left + 22, 18);
  // Separator
  doc.setDrawColor(224, 224, 224);
  doc.setLineWidth(0.3);
  doc.line(PDF_MARGINS.left, 22, pageWidth - PDF_MARGINS.right, 22);
}

function renderPageFooter(doc: jsPDF, pageNum: number): void {
  const pageHeight = doc.internal.pageSize.getHeight();
  const pageWidth = doc.internal.pageSize.getWidth();
  doc.setDrawColor(224, 224, 224);
  doc.setLineWidth(0.3);
  doc.line(PDF_MARGINS.left, pageHeight - 18, pageWidth - PDF_MARGINS.right, pageHeight - 18);
  doc.setFont('helvetica', 'italic');
  doc.setFontSize(7);
  doc.setTextColor(150, 150, 150);
  doc.text('SIT-HR - System-generated HR guidance, not legal advice. Review before acting.', PDF_MARGINS.left, pageHeight - 12);
  doc.text(`Page ${pageNum}`, pageWidth - PDF_MARGINS.right, pageHeight - 12, { align: 'right' });
}

function renderBlocks(doc: jsPDF, blocks: MarkdownBlock[], startY: number, indent: number = 0): number {
  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();
  const leftEdge = PDF_MARGINS.left + indent;
  const contentWidth = pageWidth - leftEdge - PDF_MARGINS.right;
  let y = startY;

  function needsNewPage(needed: number): boolean {
    return y + needed > pageHeight - PDF_MARGINS.bottom;
  }

  function newPage(): void {
    doc.addPage();
    renderPageHeader(doc);
    y = PDF_MARGINS.top;
  }

  for (const block of blocks) {
    switch (block.type) {
      case 'h1': {
        if (needsNewPage(20)) newPage();
        y += 6;
        doc.setFont('helvetica', 'bold');
        doc.setFontSize(15);
        doc.setTextColor(28, 28, 30);
        const lines = doc.splitTextToSize(block.text || '', contentWidth);
        doc.text(lines, leftEdge, y);
        y += lines.length * 8 + 2;
        doc.setDrawColor(45, 125, 210);
        doc.setLineWidth(0.5);
        doc.line(leftEdge, y, leftEdge + contentWidth, y);
        y += 5;
        break;
      }
      case 'h2': {
        if (needsNewPage(14)) newPage();
        y += 5;
        doc.setFont('helvetica', 'bold');
        doc.setFontSize(11);
        doc.setTextColor(45, 125, 210);
        const lines = doc.splitTextToSize((block.text || '').toUpperCase(), contentWidth);
        doc.text(lines, leftEdge, y);
        y += lines.length * 6 + 3;
        break;
      }
      case 'h3': {
        if (needsNewPage(12)) newPage();
        y += 3;
        doc.setFont('helvetica', 'bold');
        doc.setFontSize(10);
        doc.setTextColor(28, 28, 30);
        const lines = doc.splitTextToSize(block.text || '', contentWidth);
        doc.text(lines, leftEdge, y);
        y += lines.length * 6 + 2;
        break;
      }
      case 'paragraph': {
        doc.setFont('helvetica', 'normal');
        doc.setFontSize(9);
        doc.setTextColor(60, 60, 60);
        const lines = doc.splitTextToSize(block.text || '', contentWidth);
        if (needsNewPage(lines.length * 5 + 2)) newPage();
        doc.text(lines, leftEdge, y);
        y += lines.length * 5 + 2;
        break;
      }
      case 'bold': {
        doc.setFont('helvetica', 'bold');
        doc.setFontSize(9);
        doc.setTextColor(28, 28, 30);
        const lines = doc.splitTextToSize(block.text || '', contentWidth);
        if (needsNewPage(lines.length * 5 + 2)) newPage();
        doc.text(lines, leftEdge, y);
        y += lines.length * 5 + 2;
        break;
      }
      case 'bullet': {
        doc.setFont('helvetica', 'normal');
        doc.setFontSize(9);
        doc.setTextColor(60, 60, 60);
        const lines = doc.splitTextToSize(block.text || '', contentWidth - 8);
        if (needsNewPage(lines.length * 5 + 2)) newPage();
        doc.setFillColor(45, 125, 210);
        doc.circle(leftEdge + 2, y - 1.5, 1, 'F');
        doc.text(lines, leftEdge + 7, y);
        y += lines.length * 5 + 1.5;
        break;
      }
      case 'numbered': {
        doc.setFont('helvetica', 'normal');
        doc.setFontSize(9);
        doc.setTextColor(60, 60, 60);
        const lines = doc.splitTextToSize(block.text || '', contentWidth - 8);
        if (needsNewPage(lines.length * 5 + 2)) newPage();
        doc.setFont('helvetica', 'bold');
        doc.text(`${block.num}.`, leftEdge, y);
        doc.setFont('helvetica', 'normal');
        doc.text(lines, leftEdge + 7, y);
        y += lines.length * 5 + 1.5;
        break;
      }
      case 'blockquote': {
        doc.setFont('helvetica', 'italic');
        doc.setFontSize(9);
        doc.setTextColor(60, 60, 60);
        const lines = doc.splitTextToSize(block.text || '', contentWidth - 12);
        const boxH = lines.length * 5 + 6;
        if (needsNewPage(boxH + 4)) newPage();
        y += 1;
        doc.setFillColor(240, 247, 255);
        doc.rect(leftEdge, y - 4, contentWidth, boxH, 'F');
        doc.setDrawColor(45, 125, 210);
        doc.setLineWidth(1.5);
        doc.line(leftEdge, y - 4, leftEdge, y - 4 + boxH);
        doc.text(lines, leftEdge + 6, y);
        y += boxH + 2;
        break;
      }
      case 'divider': {
        if (needsNewPage(8)) newPage();
        y += 3;
        doc.setDrawColor(224, 224, 224);
        doc.setLineWidth(0.3);
        doc.line(leftEdge, y, leftEdge + contentWidth, y);
        y += 4;
        break;
      }
      case 'spacer': {
        y += 3;
        break;
      }
    }
  }
  return y;
}

