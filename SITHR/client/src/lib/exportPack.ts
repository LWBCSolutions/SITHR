function buildDocxFromTemplate(
  template: DocumentTemplate,
  title: string,
  filledFields: Record<string, string>,
  dateStr: string
): Document {
  const bodyChildren: Paragraph[] = [];

  // Document title
  bodyChildren.push(
    new Paragraph({
      children: [
        new TextRun({
          text: title,
          bold: true,
          font: 'Arial',
          size: 28, // 14pt
        }),
      ],
      alignment: AlignmentType.CENTER,
      spacing: { after: 200 },
    })
  );

  // Organisation name
  bodyChildren.push(
    createFieldParagraph('[INSERT: Organisation name]', { bold: true, size: 24 })
  );

  // Date
  bodyChildren.push(
    new Paragraph({
      children: [
        new TextRun({
          text: `Date: ${dateStr}`,
          font: 'Arial',
          size: 24,
        }),
      ],
      spacing: { after: 400 },
    })
  );

  // Horizontal rule (blank line separator)
  bodyChildren.push(
    new Paragraph({
      spacing: { after: 200 },
      children: [
        new TextRun({
          text: '_______________________________________________',
          font: 'Arial',
          size: 24,
          color: 'CCCCCC',
        }),
      ],
    })
  );

  // Render each template section
  for (const section of template.sections) {
    if (section.heading) {
      bodyChildren.push(
        new Paragraph({
          text: section.heading,
          heading: HeadingLevel.HEADING_2,
          spacing: { before: 300, after: 100 },
        })
      );
    }

    for (const para of section.paragraphs) {
      // Replace filled fields in the paragraph text
      let processedText = para;
      for (const [field, value] of Object.entries(filledFields)) {
        const placeholder = `[INSERT: ${field}]`;
        processedText = processedText.split(placeholder).join(value);
      }
      bodyChildren.push(createFieldParagraph(processedText));
      bodyChildren.push(new Paragraph({ spacing: { after: 100 } }));
    }
  }

  return new Document({
    sections: [
      {
        properties: {},
        headers: {
          default: new Header({
            children: [
              new Paragraph({
                children: [
                  new TextRun({
                    text: title,
                    bold: true,
                    font: 'Arial',
                    size: 18, // 9pt
                    color: '666666',
                  }),
                ],
                alignment: AlignmentType.RIGHT,
              }),
            ],
          }),
        },
        footers: {
          default: buildDocxFooter(),
        },
        children: bodyChildren,
      },
    ],
  });
}

