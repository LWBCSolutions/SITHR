/**
 * Creates a paragraph with [INSERT: ...] fields highlighted in bold red.
 */
function createFieldParagraph(
  text: string,
  options?: { bold?: boolean; size?: number }
): Paragraph {
  const parts = text.split(/(\[INSERT:[^\]]+\])/g);
  const children: TextRun[] = parts.map((part) => {
    if (part.match(/^\[INSERT:/)) {
      return new TextRun({
        text: part,
        bold: true,
        color: 'CC0000',
        font: 'Arial',
        size: options?.size || 24,
      });
    }
    return new TextRun({
      text: part,
      bold: options?.bold || false,
      font: 'Arial',
      size: options?.size || 24,
    });
  });
  return new Paragraph({ children });
}

