/** Lines starting with these prefixes are metadata and should be skipped in advisory content. */
const METADATA_PREFIXES = [
  'QUERY TYPE:',
  'SECTOR FLAG:',
  'RISK RATING:',
  'LEGAL FRAMEWORK:',
  'POLICY CHECK:',
  'PROTECTED DISCLOSURE CHECK:',
  'JANUARY 2027 FLAG:',
];

function isMetadataLine(line: string): boolean {
  const trimmed = line.trim();
  return METADATA_PREFIXES.some((prefix) => trimmed.startsWith(prefix));
}

/**
 * Extract numbered action steps from an assistant message.
 * Looks for lines like "1. Do something" that appear to be action items.
 */
function extractActionSteps(content: string): string[] {
  const lines = content.split('\n');
  const steps: string[] = [];
  for (const line of lines) {
    const match = line.match(/^\d+\.\s+(.+)$/);
    if (match) {
      const text = match[1].replace(/\*\*(.*?)\*\*/g, '$1').replace(/\*(.*?)\*/g, '$1').trim();
      if (text.length > 0) {
        steps.push(text);
      }
    }
  }
  return steps;
}

/**
 * Deduplicate action steps by checking for significant overlap.
 */
function deduplicateActions(actions: string[]): string[] {
  const unique: string[] = [];
  for (const action of actions) {
    const normalized = action.toLowerCase().replace(/[^a-z0-9 ]/g, '').trim();
    const isDuplicate = unique.some((existing) => {
      const existingNorm = existing.toLowerCase().replace(/[^a-z0-9 ]/g, '').trim();
      return existingNorm === normalized || existingNorm.includes(normalized) || normalized.includes(existingNorm);
    });
    if (!isDuplicate) {
      unique.push(action);
    }
  }
  return unique;
}

